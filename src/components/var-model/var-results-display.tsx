'use client';

import { useState } from 'react';
import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle,
} from '@/components/ui/card';
import { Skeleton } from '@/components/ui/skeleton';
import { Button } from '@/components/ui/button';
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
  DialogTrigger,
} from '@/components/ui/dialog';
import { AlertCircle, Bot, Sparkles } from 'lucide-react';
import type { VaRResult } from '@/lib/types';
import { explainVaRResult } from '@/ai/flows/explain-var-result';
import { useToast } from '@/hooks/use-toast';

type VarResultsDisplayProps = {
  varResults: VaRResult | null;
  isCalculating: boolean;
};

const formatCurrency = (value: number) =>
  new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    maximumFractionDigits: 0,
  }).format(value);

const formatPercentage = (value: number) =>
  `${value.toFixed(2)}%`;

export function VarResultsDisplay({
  varResults,
  isCalculating,
}: VarResultsDisplayProps) {
  const { toast } = useToast();
  const [explanation, setExplanation] = useState('');
  const [isExplaining, setIsExplaining] = useState(false);
  const [dialogOpen, setDialogOpen] = useState(false);
  const hasResults = varResults && varResults.portfolioValue > 0;

  const handleExplainResults = async () => {
    if (!varResults) return;
    setIsExplaining(true);
    setDialogOpen(true);
    try {
      const result = await explainVaRResult({
        ...varResults,
        confidenceInterval: 0.95,
      });
      setExplanation(result.explanation);
    } catch (error) {
      console.error('Error explaining VaR results:', error);
      toast({
        title: 'Explanation Failed',
        description: 'Could not get an explanation from the AI model.',
        variant: 'destructive',
      });
      setDialogOpen(false);
    } finally {
      setIsExplaining(false);
    }
  };

  const metrics = [
    { label: 'Portfolio Value', value: varResults?.portfolioValue, formatter: formatCurrency },
    { label: 'Absolute VaR (95%)', value: varResults?.absoluteVaR, formatter: formatCurrency },
    { label: 'Relative VaR', value: varResults?.relativeVaR, formatter: formatCurrency },
    { label: 'VaR % of NAV', value: varResults?.varPercentageOfNAV, formatter: formatPercentage },
    { label: 'Component VaR', value: varResults?.componentVaR, formatter: formatCurrency },
    { label: 'Conditional VaR (95%)', value: varResults?.conditionalVaR, formatter: formatCurrency },
  ];

  return (
    <Card>
      <CardHeader className="flex flex-row items-center justify-between">
        <div>
          <CardTitle>Value at Risk (VaR) Results</CardTitle>
          <CardDescription>
            Potential loss in value of the portfolio over a defined period for a given confidence interval.
          </CardDescription>
        </div>
        <Dialog open={dialogOpen} onOpenChange={setDialogOpen}>
          <DialogTrigger asChild>
            <Button
              variant="outline"
              disabled={isCalculating || !hasResults}
              onClick={handleExplainResults}
            >
              <Sparkles className="mr-2 h-4 w-4" />
              Explain Results
            </Button>
          </DialogTrigger>
          <DialogContent className="sm:max-w-[625px]">
            <DialogHeader>
              <DialogTitle className="flex items-center gap-2">
                <Bot /> AI Explanation of VaR Results
              </DialogTitle>
              <DialogDescription>
                This is a simplified explanation generated by an AI model.
              </DialogDescription>
            </DialogHeader>
            <div className="prose prose-sm max-w-none text-foreground dark:prose-invert">
              {isExplaining ? (
                <div className="space-y-2">
                  <Skeleton className="h-4 w-full" />
                  <Skeleton className="h-4 w-full" />
                  <Skeleton className="h-4 w-3/4" />
                </div>
              ) : (
                <p style={{ whiteSpace: 'pre-wrap' }}>{explanation}</p>
              )}
            </div>
          </DialogContent>
        </Dialog>
      </CardHeader>
      <CardContent>
        {!hasResults ? (
          <div className="flex min-h-[120px] items-center justify-center rounded-lg border-2 border-dashed bg-muted/50 p-6 text-center">
            <div className="flex flex-col items-center gap-2 text-muted-foreground">
              <AlertCircle className="h-8 w-8" />
              <p className="font-medium">No results to display</p>
              <p className="text-sm">
                Add instruments to your portfolio to calculate VaR.
              </p>
            </div>
          </div>
        ) : (
          <div className="grid grid-cols-2 gap-4 md:grid-cols-3 lg:grid-cols-6">
            {metrics.map((metric) => (
              <div
                key={metric.label}
                className="rounded-lg border bg-card p-4 text-center shadow-sm"
              >
                <p className="text-sm text-muted-foreground">{metric.label}</p>
                {isCalculating ? (
                  <Skeleton className="mx-auto mt-1 h-7 w-2/3" />
                ) : (
                  <p className="text-2xl font-bold text-primary">
                    {metric.value !== undefined ? metric.formatter(metric.value) : '-'}
                  </p>
                )}
              </div>
            ))}
          </div>
        )}
      </CardContent>
    </Card>
  );
}
